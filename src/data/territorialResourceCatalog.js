export function findTerritoryByCommuneLabel(communeLabel) {
  const commune = communes.find((item) => item.label === communeLabel);
  if (!commune) return null;
  return territories.find((item) => item.id === commune.territoryId) || null;
}

export function findTerritoryByCommuneId(communeId) {
  const commune = communes.find((item) => item.id === communeId);
  if (!commune) return null;
  return territories.find((item) => item.id === commune.territoryId) || null;
}

export function getSubcategoryLabel(subcategoryId) {
  return subcategories.find((item) => item.id === subcategoryId)?.label || "";
}

export function getCategoryLabel(categoryId) {
  return categories.find((item) => item.id === categoryId)?.label || "";
}

export function getProfileLabel(profileId) {
  return patientProfiles.find((item) => item.id === profileId)?.label || "";
}

export function getUrgencyLabel(urgencyId) {
  return urgencyLevels.find((item) => item.id === urgencyId)?.label || "";
}

export function getResourcesByTerritory(territoryId) {
  return resources.filter(
    (item) => item.territoryId === territoryId || item.territoryId === "T_DEPT"
  );
}

export function getResourcesBySubcategoryAndTerritory(subcategoryId, territoryId) {
  return resources.filter(
    (item) =>
      item.subcategoryId === subcategoryId &&
      (item.territoryId === territoryId || item.territoryId === "T_DEPT")
  );
}